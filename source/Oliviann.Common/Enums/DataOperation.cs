namespace Oliviann
{
    /// <summary>
    /// Enumeration type for defining the data operation type.
    /// </summary>
    public enum DataOperation
    {
        /// <summary>
        /// Designates a create operation.
        /// </summary>
        Create,

        /// <summary>
        /// Designates a read operation.
        /// </summary>
        Read,

        /// <summary>
        /// Designates a update operation.
        /// </summary>
        Update,

        /// <summary>
        /// Designates a delete operation.
        /// </summary>
        Delete
    }
}